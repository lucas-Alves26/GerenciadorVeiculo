CREATE TABLE TBL_ENDERECO
(
 END_INT_ID INT NOT NULL IDENTITY(1,1),
 CID_INT_ID INT NOT NULL,
 EST_INT_CODUF INT NOT NULL,
 END_STR_RUA VARCHAR(60) NOT NULL,
