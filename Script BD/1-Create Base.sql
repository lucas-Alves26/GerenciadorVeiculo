CREATE TABLE TBL_CIDADE
(
  CID_INT_ID int NOT NULL,
  CID_STR_NOME varchar(120) NOT NULL,
  EST_INT_CODUF int NOT NULL,
  CID_INT_IBGE int NOT NULL
)
---PRIMARY KEY
ALTER TABLE TBL_CIDADE
